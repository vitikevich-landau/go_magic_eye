// Package text — примитивы текста и цвета: ANSI-палитра, ширина рун,
// обрезка с учётом escape-последовательностей, hex, строитель строк Line.
//
// Пакет ничего не знает ни о рефлексии, ни о терминале — только буквы.
package text

import "sync/atomic"

// Глобальные выключатели цвета и ASCII-рамок. Атомики, а не голые bool:
// loadConfig выставляет их на КАЖДЫЙ вызов Inspect/Finspect, и конкурентные
// осмотры из разных горутин (законный сценарий, особенно в JSON-режиме на
// сервере) иначе гонялись бы на записи.
var (
	colorOn atomic.Bool
	asciiOn atomic.Bool
)

// SetColor — глобальный выключатель цвета (EYE_COLOR=1/0 или автоопределение
// терминала). Когда выключен, Paint отдаёт строку как есть.
func SetColor(on bool) { colorOn.Store(on) }

// ColorOn — включён ли цвет.
func ColorOn() bool { return colorOn.Load() }

// SetASCII — рисовать рамки и стрелки чистым ASCII (EYE_ASCII=1).
func SetASCII(on bool) { asciiOn.Store(on) }

// ASCIIOn — включён ли режим чистого ASCII.
func ASCIIOn() bool { return asciiOn.Load() }

// Стили палитры «Гримуар» (256-цветный ANSI). Имена — по роли, не по цвету.
//
// Серая лестница разведена нарочно: ДАННЫЕ ярче декора минимум на несколько
// ступеней (hex 252 » заметки 244 » offset'ы 240 » рамки 238) — иначе байты,
// ради которых карта памяти существует, сливаются с рамками и подписями.
const (
	CReset = "\x1b[0m"

	CFrame = "\x1b[38;5;238m"   // рамки, спайны дерева (самый тихий серый)
	CTitle = "\x1b[1;38;5;229m" // заголовки-картуши
	CName  = "\x1b[38;5;117m"   // имена полей
	CType  = "\x1b[38;5;180m"   // имена типов
	CVal   = "\x1b[38;5;150m"   // значения
	CPad   = "\x1b[38;5;167m"   // padding-дыры
	CItab  = "\x1b[38;5;176m"   // interface-слова, itab, методы
	CHex   = "\x1b[38;5;252m"   // hex-байты (главные данные — самый яркий серый)
	CAscii = "\x1b[38;5;72m"    // ascii-колонка дампа (зелёно-циановый, не путается с адресами)
	CAddr  = "\x1b[38;5;110m"   // адреса
	CNote  = "\x1b[38;5;244m"   // заметки, уроки
	CWarn  = "\x1b[38;5;203m"   // честные отказы, ловушки
	COk    = "\x1b[38;5;114m"   // хорошие новости
	COff   = "\x1b[38;5;240m"   // колонка offset'ов
	CDim   = "\x1b[2m"          // приглушить
	CSel   = "\x1b[7m"          // курсор TUI (инверсия)
	CBold  = "\x1b[1m"
	CFocus = "\x1b[38;5;178m" // золото: разделитель и метки со стороны фокуса
)

// Paint красит строку стилем, если цвет включён.
func Paint(style, s string) string {
	if !ColorOn() || style == "" || s == "" {
		return s
	}
	return style + s + CReset
}
